# Syntax

print(type(2))  # int
print(type(2.1))  # float
print(type("a"))  # str
print(type(True))  # bool
print(type({"3", "b"}))  # set
print(type(["3", "b"]))  # list
print(type((3, 1)))  # tuple
print(type({"hi": "there", "b": "there"}))  # dict

# Int & Float

print(4 + 2)  # 6
print(4 - 2)  # 2
print(-2 + 2)  # 0
print(2 / 4)  # 0.5
print(2 // 4)  # 0
print(4 * 2)  # 8
print(2 ** 2)  # 4
print(5 % 4)  # 1 (remainder)

# Math function

print(round(3.14, 1))  # 3.1
print(abs(-3.14))  # 3.14

# Operator precedence

